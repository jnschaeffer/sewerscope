CREATE POLICY "Enable insert for authenticated users only" ON "public"."reports" FOR INSERT TO "authenticated" WITH CHECK (true);



CREATE POLICY "Enable insert for user's own reports only" ON "public"."images" FOR INSERT TO "authenticated" WITH CHECK ((( SELECT "auth"."uid"() AS "uid") = ( SELECT "reports"."user_id"
   FROM "public"."reports"
  WHERE ("reports"."id" = "images"."report_id")
 LIMIT 1)));



