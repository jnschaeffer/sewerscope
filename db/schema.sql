CREATE POLICY "Enable read access for all users" ON "public"."inlets" FOR SELECT USING (true);



CREATE POLICY "Enable users to view their own data only" ON "public"."reports" FOR SELECT TO "authenticated" USING ((( SELECT "auth"."uid"() AS "uid") = "user_id"));



